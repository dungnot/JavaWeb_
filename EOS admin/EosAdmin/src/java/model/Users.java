package model;

import java.sql.Date;

/**
 *
 * @author DÅ©ng
 */
public class Users {

    int id;
    String user_name;
    String password;
    String email;
    Date dob;

    public Users(int id, String user_name, String password, String email, Date dob) {
        this.id = id;
        this.user_name = user_name;
        this.password = password;
        this.email = email;
        this.dob = dob;
    }

    public Users() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

}
